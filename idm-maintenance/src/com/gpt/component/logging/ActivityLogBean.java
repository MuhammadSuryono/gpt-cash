package com.gpt.component.logging;

public class ActivityLogBean {
	private String no;
	private String latestActivity;
	private String referenceNo;
	private String menuName;
	private String corpAccount;
	private String trxAmount;
	private String status;
	private String fileFormat;
	private String activityType;
	private String activityBy;
	private String description;
	
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public String getLatestActivity() {
		return latestActivity;
	}
	public void setLatestActivity(String latestActivity) {
		this.latestActivity = latestActivity;
	}
	public String getReferenceNo() {
		return referenceNo;
	}
	public void setReferenceNo(String referenceNo) {
		this.referenceNo = referenceNo;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getCorpAccount() {
		return corpAccount;
	}
	public void setCorpAccount(String corpAccount) {
		this.corpAccount = corpAccount;
	}
	public String getTrxAmount() {
		return trxAmount;
	}
	public void setTrxAmount(String trxAmount) {
		this.trxAmount = trxAmount;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getFileFormat() {
		return fileFormat;
	}
	public void setFileFormat(String fileFormat) {
		this.fileFormat = fileFormat;
	}
	public String getActivityType() {
		return activityType;
	}
	public void setActivityType(String activityType) {
		this.activityType = activityType;
	}
	public String getActivityBy() {
		return activityBy;
	}
	public void setActivityBy(String activityBy) {
		this.activityBy = activityBy;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	
}
