package com.gpt.product.gpcash.corporate.transaction.va.report.bean;

public class TransactionVAData {

	private String no;
	private String vaNo;
	private String vaName;
	private String postDate;
	private String amount;
	private String description;
	private String fileFormat;
	
	public String getVaNo() {
		return vaNo;
	}
	public void setVaNo(String vaNo) {
		this.vaNo = vaNo;
	}
	public String getVaName() {
		return vaName;
	}
	public void setVaName(String vaName) {
		this.vaName = vaName;
	}
	public String getPostDate() {
		return postDate;
	}
	public void setPostDate(String postDate) {
		this.postDate = postDate;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getFileFormat() {
		return fileFormat;
	}
	public void setFileFormat(String fileFormat) {
		this.fileFormat = fileFormat;
	}
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	
	
}
